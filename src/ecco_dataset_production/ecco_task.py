"""ECCO Task descriptor wrapper class.

This module provides the ECCOTask class which wraps task dictionaries generated
by create_job_task_list, providing convenient property-based access and
validation methods for task attributes.
"""

import json

from . import aws
from . import ecco_file


class ECCOTask(dict):
    """Wrapper class for storage of, and operations on, ECCO task descriptors.

    ECCOTask extends the built-in dict class to provide property-based access
    to task attributes and utility methods for querying task properties. Task
    descriptors are typically generated by
    :func:`~ecco_dataset_production.apps.create_job_task_list.create_job_task_list`
    and contain all information needed to produce a single ECCO granule.

    The underlying dictionary contains the following keys:

    **Primary Keys:**

    * ``granule`` (str): Output granule file path, either a local filesystem
      path or an S3 URI (e.g., ``s3://bucket/path/file.nc``).
    * ``variables`` (dict): Mapping of variable names to their input file
      lists. Each variable maps to a list of component file groups, where
      each group is a list of ``.data``/``.meta`` file pairs.
    * ``ecco_cfg_loc`` (str): Path to the ECCO Dataset Production YAML
      configuration file.
    * ``ecco_grid_loc`` (str): Path to directory or archive containing ECCO
      grid files (XC, YC, native grid NetCDF, etc.).
    * ``ecco_mapping_factors_loc`` (str): Path to directory containing sparse
      matrix mapping factors for native-to-latlon transformations.
    * ``ecco_metadata_loc`` (str): Path to directory containing ECCO metadata
      JSON files for variable attributes, coordinates, and global metadata.
    * ``dynamic_metadata`` (dict): Time-varying and dataset-specific metadata
      (see below).

    **Dynamic Metadata Keys** (``task['dynamic_metadata']``):

    * ``name`` (str): Human-readable dataset name.
    * ``dimension`` (str): Spatial dimensionality, either ``'2D'`` or ``'3D'``.
    * ``time_coverage_start`` (str): ISO 8601 start time of averaging period.
    * ``time_coverage_end`` (str): ISO 8601 end time of averaging period.
    * ``time_coverage_center`` (str): ISO 8601 center time of averaging period.
    * ``time_long_name`` (str): Description of time coordinate meaning
      (e.g., ``'center time of averaging period'``).
    * ``time_coverage_duration`` (str): ISO 8601 duration code:
      ``'P1D'`` (daily), ``'P1M'`` (monthly), or ``'PT0S'`` (snapshot).
    * ``time_coverage_resolution`` (str): ISO 8601 temporal resolution.
    * ``summary`` (str): Dataset description for NetCDF global attributes.
    * ``comment`` (str, optional): Additional comments for the dataset.
    * ``field_components`` (dict, optional): For vector-transformed variables,
      maps variable names to their x/y component field names
      (e.g., ``{'EVEL': {'x': 'UVEL', 'y': 'VVEL'}}``).
    * ``field_orientations`` (dict, optional): For vector-transformed variables,
      maps variable names to their orientation (``'zonal'`` or ``'meridional'``).

    Args:
        task (str or dict): Either a path to a JSON file containing a single
            task descriptor, or a dictionary containing the task definition.

    Raises:
        ValueError: If task file contains multiple tasks, or if task is not
            a valid string path or dictionary.

    Example:
        >>> task = ECCOTask('/path/to/task.json')
        >>> task = ECCOTask({'granule': '...', 'variables': {...}, ...})
        >>> print(task.is_latlon)
        True
        >>> print(task.variable_names)
        ['SSH', 'SSHIBC']

    """
    def __init__(self, task=None):
        """Create instance of ECCOTask class.

        Args:
            task (str or dict, optional): (Path and) filename of JSON-formatted
                file defining a single task, or a single task description
                dictionary. If None, creates an empty task.

        Raises:
            ValueError: If task file contains multiple tasks, or if task
                argument is not a string or dictionary.

        """
        super().__init__()
        if task:
            if isinstance(task, str):
                # assume (path and) filename:
                t = json.load(open(task))
                if len(t) > 1:
                    raise ValueError('Task file input option cannot define multiple tasks')
                self.update(t[0])
            elif isinstance(task, dict):
                # assume parsed or valid task dictionary for single task:
                self.update(task)
            else:
                raise ValueError('If provided, task must be a single task (path and) filename or dict')


    @property
    def averaging_period(self):
        """str: Averaging period code extracted from granule filename.

        Returns the averaging period portion of the granule filename, such as
        ``'day_mean'``, ``'mon_mean'``, or ``'snap'``.

        """
        return ecco_file.ECCOGranuleFilestr(self.__getitem__('granule')).averaging_period


    @property
    def grid_type(self):
        """str: Grid type extracted from granule filename.

        Returns either ``'latlon'`` or ``'native'`` based on the granule
        filename convention.

        """
        return ecco_file.ECCOGranuleFilestr(self.__getitem__('granule')).grid_type


    @property
    def is_2d(self):
        """bool: True if the task produces a 2D (surface) dataset.

        Determined by checking if ``dynamic_metadata['dimension']`` equals
        ``'2D'`` (case-insensitive).

        """
        return "2d" == self.__getitem__('dynamic_metadata')['dimension'].lower()


    @property
    def is_3d(self):
        """bool: True if the task produces a 3D (depth-varying) dataset.

        Determined by checking if ``dynamic_metadata['dimension']`` equals
        ``'3D'`` (case-insensitive).

        """
        return "3d" == self.__getitem__('dynamic_metadata')['dimension'].lower()


    @property
    def is_ecco_grid_dir_local(self):
        """bool: True if the ECCO grid directory is a local filesystem path.

        Returns False if ``ecco_grid_dir`` is an S3 URI.

        """
        if aws.utils.is_s3_uri(self.__getitem__('ecco_grid_dir')):
            return False
        else:
            return True


    @property
    def is_granule_local(self):
        if aws.utils.is_s3_uri(self.__getitem__('granule')):
            return False
        else:
            return True


    @property
    def is_latlon(self):
        """bool: True if the task produces a lat/lon gridded dataset.

        Determined by parsing the granule filename to extract the grid type.

        """
        return ecco_file.ECCOGranuleFilestr(self.__getitem__('granule')).grid_type == 'latlon'


    @property
    def is_native(self):
        """bool: True if the task produces a native LLC90 gridded dataset.

        Determined by parsing the granule filename to extract the grid type.

        """
        return ecco_file.ECCOGranuleFilestr(self.__getitem__('granule')).grid_type == 'native'


    @property
    def variable_names(self):
        """list: List of variable names included in this task.

        Returns the keys from the ``variables`` dictionary, representing all
        NetCDF variables that will be included in the output granule.

        Returns:
            list: Variable name strings, or empty list if no variables defined.

        """
        try:
            return list(self.__getitem__('variables').keys())
        except:
            return []


    def is_variable_single_component(self, variable=None):
        """Check if a variable has a single input component.

        Single-component variables are read directly from MDS files. Multi-
        component variables (e.g., vector fields) require transformation
        from their constituent components (e.g., UVEL/VVEL to EVEL/NVEL).

        Args:
            variable (str): Variable name to check.

        Returns:
            bool: True if the variable has only one input component group,
            False if it requires multiple components (vector transformation).

        """
        if len(self.__getitem__('variables')[variable]) == 1:
            return True
        else:
            return False


    def is_variable_input_local(self, variable=None):
        """Check if a variable's input files are stored locally.

        Examines the first input file path to determine if inputs are local
        filesystem paths or S3 URIs. Assumes all inputs for a variable are
        stored in the same location (all local or all S3).

        Args:
            variable (str): Variable name to check.

        Returns:
            bool: True if input files are local filesystem paths, False if
            they are S3 URIs requiring download.

        """
        # just look at first file in list (reasonable assumption is that all are
        # local or all are remote (s3://...))

        if aws.utils.is_s3_uri(self.__getitem__('variables')[variable][0][0]):
            return False
        else:
            return True


    def variable_inputs(self, variable=None):
        """Get the input file list for a specified variable.

        Returns the nested list structure of input files for the variable.
        For single-component variables, this is a list containing one list
        of ``.data``/``.meta`` file pairs. For vector-transformed variables,
        this contains multiple component file groups.

        Args:
            variable (str): Variable name to retrieve inputs for.

        Returns:
            list: Nested list of input file paths. Structure is::

                [
                    [  # Component 1 (or only component)
                        ['/path/to/file.data', '/path/to/file.meta'],
                        ...
                    ],
                    [  # Component 2 (for vector fields)
                        ['/path/to/file.data', '/path/to/file.meta'],
                        ...
                    ]
                ]

            Returns empty list if variable not found.

        """
        try:
            return self.__getitem__('variables')[variable]
        except:
            return []
